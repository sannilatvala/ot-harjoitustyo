# colors in RGB

class Colors:
    def __init__(self):
        pass

    def black(self):
        return (0, 0, 0)

    def dim_grey(self):
        return (105, 105, 105)

    def grey(self):
        return (128, 128, 128)

    def lavender(self):
        return (230, 230, 250)

    def medium_slate_blue(self):
        return (123, 104, 238)

    def lime_green(self):
        return (50, 205, 50)

    def white(self):
        return (255, 255, 255)
